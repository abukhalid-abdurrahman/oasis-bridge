namespace RadixBridge.Enums;

/// <summary>
/// Defines the types of addresses supported in the Radix network.
/// </summary>
public enum AddressType
{
    /// <summary>
    /// Represents an account address, which is used for holding and transferring tokens.
    /// </summary>
    Account,

    /// <summary>
    /// Represents an identity address, which is used for managing identities and permissions.
    /// </summary>
    Identity
}